import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(fileURLToPath(new URL("..", import.meta.url)));
const distDir = path.join(rootDir, "dist");
const distIndexPath = path.join(distDir, "index.html");
const outputPath = path.join(rootDir, "letterboxd-distribution-designer.html");

function resolveDistAsset(assetPath) {
  const normalizedPath = assetPath.replace(/^[./\\]+/, "").replace(/^\/+/, "");
  return path.join(distDir, normalizedPath);
}

async function inlineStyles(html) {
  const stylesheetPattern = /<link\b(?=[^>]*rel=["']stylesheet["'])(?=[^>]*href=["']([^"']+)["'])[^>]*>/g;
  const matches = [...html.matchAll(stylesheetPattern)];
  let nextHtml = html;

  for (const match of matches) {
    const [tag, href] = match;
    const css = await readFile(resolveDistAsset(href), "utf8");
    nextHtml = nextHtml.replace(tag, () => `<style>\n${css}\n    </style>`);
  }

  return nextHtml;
}

async function inlineModuleScripts(html) {
  const scriptPattern = /<script\b(?=[^>]*type=["']module["'])(?=[^>]*src=["']([^"']+)["'])[^>]*><\/script>/g;
  const matches = [...html.matchAll(scriptPattern)];
  let nextHtml = html;

  for (const match of matches) {
    const [tag, src] = match;
    const js = await readFile(resolveDistAsset(src), "utf8");
    nextHtml = nextHtml.replace(tag, () => `<script type="module">\n${js}\n    </script>`);
  }

  return nextHtml;
}

function removeModulePreloads(html) {
  return html.replace(/<link\b(?=[^>]*rel=["']modulepreload["'])[^>]*>\s*/g, "");
}

function addGeneratedNotice(html) {
  const notice =
    "<!-- Generated by npm run build:standalone. Edit source files, not this file. -->";
  return html.replace("<!doctype html>", `<!doctype html>\n${notice}`);
}

function assertStandalone(html) {
  const unresolvedAssetPattern = /\b(?:href|src)=["'][^"']*(?:\/assets\/|\.\/assets\/|assets\/)/;
  if (unresolvedAssetPattern.test(html)) {
    throw new Error("Standalone HTML still references generated asset files.");
  }
}

let html = await readFile(distIndexPath, "utf8");
html = removeModulePreloads(html);
html = await inlineStyles(html);
html = await inlineModuleScripts(html);
html = addGeneratedNotice(html);
html = html.replace(/\r\n?/g, "\n");
assertStandalone(html);

await writeFile(outputPath, html);
console.log(`Wrote ${path.relative(rootDir, outputPath)}`);
